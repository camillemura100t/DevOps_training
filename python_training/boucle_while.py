n = 10

while n >= 1:
    if n == 1:
        print("C'est dans %d an je m'en irai, j'entends le loup et le renard chanter" %n)
    else:
        print("C'est dans %d ans je m'en irai, j'entends le loup et le renard chanter" %n)
    n = n - 1
